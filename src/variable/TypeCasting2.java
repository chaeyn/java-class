package variable;

public class TypeCasting2 {
    public static void main(String[] args) {
        long value5 = 10L;
        long value6 = 10l;
        float value7 = 10F;
        double value8 = 10f;

        System.out.println("value1 = " + value5);
        System.out.println("value2 = " + value6);
        System.out.println("value3 = " + value7);
        System.out.println("value4 = " + value8);
    }
}
